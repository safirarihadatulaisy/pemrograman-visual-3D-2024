    private void SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanActionPerformed
        String nama = Nama.getText();
        String tglLahir = TanggalLahir.getText();
        String alamat = Alamat.getText();
        String email = Email.getText();
        String hp = NoHandphone.getText();
        String jenisKelamin;
        if (JenisKelaminLaki.isSelected()) {
            jenisKelamin = "Laki laki";
        } else {
            jenisKelamin = "Perempuan";
        }
        String prodi = (String) Prodi.getSelectedItem();
        
        if(nama.isEmpty() || tglLahir.isEmpty() || alamat.isEmpty() || email.isEmpty() || hp.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Anda belum mengisi Semua Form", "Peringatan", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            int noHp = Integer.parseInt(hp);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "anda harus memasukkan angka ke dalam no hp", "peringatan", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(!pemastian.isSelected()) {
            JOptionPane.showMessageDialog(null, "Anda belum mencentang", "Peringatan", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Halaman2 halaman2 = new Halaman2();
        halaman2.tampilkanData(nama, tglLahir, alamat, email, hp, jenisKelamin, prodi);
        halaman2.setVisible(true);
    }//GEN-LAST:event_SimpanActionPerformed

