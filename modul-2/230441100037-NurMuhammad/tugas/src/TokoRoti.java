    private void BeliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BeliActionPerformed
        if (menuPesanan != "" || totalSemua != 0 ) {
            int cash = 
                (Cash.getText().isEmpty())
                    ? 0 
                    : Integer.parseInt(Cash.getText().replace(",", "").replace(".", ""));
            if (cash < totalSemua) {
                JOptionPane.showMessageDialog(null, "Uang anda tidak cukup!", "Peringatan", JOptionPane.ERROR_MESSAGE);
                return;
            }
            int pilihan = JOptionPane.showConfirmDialog(null, "Anda yakin ingin membeli?", "peringatan", JOptionPane.YES_NO_OPTION);
            if (pilihan == JOptionPane.YES_OPTION) {
                clear();
                totalSemua = 0;
                Pesanan.setText("");
                textPesanan = "";
                TFTotalSemua.setText("0");

                kurang.setText(" ");
                Cash.setText("0");
                Kembalian.setText("0");
            }
        }
    }//GEN-LAST:event_BeliActionPerformed

    private void hitungTotal() {
        int menu;
        if (MRotiGandum.isSelected()) {
            menuPesanan = "Roti Gandum";
            menu = 17000;
        } else if (MRotiIsi.isSelected()) {
            menuPesanan = "Roti Isi";
            menu = 20000;
        } else if (MRotiCoklat.isSelected()) {
            menuPesanan = "Roti Coklat";
            menu = 18000;
        } else if (MRotiKeju.isSelected()) {
            menuPesanan = "Roti Keju";
            menu = 25000;
        } else if (MRotiPisang.isSelected()) {
            menuPesanan = "Roti Pisang";
            menu = 23000;
        } else if (MRotiTawar.isSelected()) {
            menuPesanan = "Roti Tawar";
            menu = 15000;
        } else {
            menuPesanan = "";
            menu = 0;
        }
        
        if (menu != 0) {
            TPCoklat.setEnabled(true);
            TPKacang.setEnabled(true);
            TPKeju.setEnabled(true);
            TPMeses.setEnabled(true);
        }
        
        int topping;
        if (TPCoklat.isSelected()) {
            toppingPesanan = " + Topping Coklat";
            topping = 5000;
        } else if (TPKeju.isSelected()) {
            toppingPesanan = " + Topping Keju";
            topping = 3000;
        } else if (TPKacang.isSelected()) {
            toppingPesanan = " + Topping Kacang";
            topping = 4000;
        } else if (TPMeses.isSelected()) {
            toppingPesanan = " + Topping Meses";
            topping = 4000;
        } else {
            toppingPesanan = "";
            topping = 0;
        }
        
        int jumlahPesanan = Integer.parseInt(TFJumlah.getText().replace(",", ""));
        int total = (menu + topping) * jumlahPesanan;
        TFTotal.setText(formatter.format(total));        
        
        if (totalSemua == 0) {
            TFTotalSemua.setText(
                formatter.format((total > 100000) 
                    ? total - (total * 5 / 100) 
                    : total
                )
            );
            Pesanan.setText(
                menuPesanan + 
                toppingPesanan + 
                ", Jumlah: " + formatter.format(Integer.parseInt(TFJumlah.getText().replace(",", ""))) + 
                ", Total: " + formatter.format(Integer.parseInt(TFTotal.getText().replace(",", "")))
            );
        } else {
            TFTotalSemua.setText(
                formatter.format(
                    ((totalSemua + total) > 100000) 
                        ? (totalSemua + total) - ((totalSemua + total) * 5 / 100) 
                        : (totalSemua + total)
                )
            );
            Pesanan.setText(
                textPesanan + 
                menuPesanan + 
                toppingPesanan + 
                ", Jumlah: " + formatter.format(Integer.parseInt(TFJumlah.getText().replace(",", ""))) + 
                ", Total: " + formatter.format(Integer.parseInt(TFTotal.getText().replace(",", "")))
            );
        }
        hitungKembalian();
    }
    
    private void hitungKembalian() {
        try {
            int cash = 
                (Cash.getText().isEmpty())
                    ? 0 
                    : Integer.parseInt(Cash.getText().replace(",", "").replace(".", ""));
            int total = Integer.parseInt(TFTotalSemua.getText().replace(",", ""));

            if(cash < total) {
                kurang.setText("Kurang " + formatter.format(Math.abs(cash-total)));
                Kembalian.setText("0");
            } else {
                kurang.setText(" ");
                Kembalian.setText(formatter.format(cash-total));
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Input harus berupa angka!", "Peringatan", JOptionPane.ERROR_MESSAGE);
        }
    }
