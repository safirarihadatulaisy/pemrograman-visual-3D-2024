    private void TambahPesananActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TambahPesananActionPerformed
        if (!(menuPesanan == "" || menuPesanan == null)) {
            totalSemua += Integer.parseInt(TFTotal.getText().replace(",", ""));
            TFTotalSemua.setText(
                formatter.format((totalSemua > 100000) 
                    ? totalSemua - (totalSemua * 5 / 100) 
                    : totalSemua
                )
            );
            textPesanan += 
                menuPesanan + 
                toppingPesanan + 
                ", Jumlah: " + formatter.format(Integer.parseInt(TFJumlah.getText().replace(",", ""))) + 
                ", Total: " + formatter.format(Integer.parseInt(TFTotal.getText().replace(",", ""))) + "\n";
            Pesanan.setText(textPesanan);
            clear();
        }
    }//GEN-LAST:event_TambahPesananActionPerformed
