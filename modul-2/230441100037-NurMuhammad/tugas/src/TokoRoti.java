    private void TFJumlahKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TFJumlahKeyReleased
        try {
            String text = TFJumlah.getText().replace(",", "");
            if (text.isEmpty()) {
                BKurangJumlah.setEnabled(false);
            } else {
                int jumlah = Integer.parseInt(text);
                if (jumlah > 0) {
                    BKurangJumlah.setEnabled(jumlah > 1);
                    
                    TFJumlah.setText(Integer.toString(jumlah));
                    if (Integer.parseInt(TFTotal.getText().replace(",", "")) != 0) {
                        hitungTotal();
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Input harus angka positif!", "Peringatan", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Input harus berupa angka!", "Peringatan", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_TFJumlahKeyReleased
    private void BKurangJumlahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BKurangJumlahActionPerformed
        try {
            int jumlah = TFJumlah.getText().isEmpty() ? 0 : Integer.parseInt(TFJumlah.getText().replace(",", ""));
            jumlah--;
            TFJumlah.setText(Integer.toString(jumlah));
            if (Integer.parseInt(TFTotal.getText().replace(",", "")) != 0) {
                hitungTotal();
            }
            if (jumlah <= 1 ) {
                BKurangJumlah.setEnabled(false);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "anda harus menginputkan angka", "peringatan", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_BKurangJumlahActionPerformed
