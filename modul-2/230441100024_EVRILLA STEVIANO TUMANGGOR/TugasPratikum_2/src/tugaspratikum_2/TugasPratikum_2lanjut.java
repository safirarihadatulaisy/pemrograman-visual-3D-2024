    private void bayar(){
        try{
            int Cash = Integer.parseInt(cash.getText());
            int Total = Integer.parseInt(total.getText());
            int Kembalian = Cash - Total;
            
            if (Cash < Total){
                kembalian.setText("Uang anda tidak cukup!");
            }else{
                kembalian.setText(String.valueOf("Rp. " + Kembalian));
                
                int Reset = JOptionPane.showConfirmDialog(null, "Terima kasih telah memesan!", "Informasi",
                        JOptionPane.OK_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE);
                
                if (Reset == JOptionPane.OK_OPTION){
                    kembalian.setText("");
                    total.setText("");
                    jumlah.setText("0");
                    cash.setText("");
                    buttonGroup1.clearSelection();
                    buttonGroup2.clearSelection();
                }
            }
            
            
            
            
        } catch (NumberFormatException e){
            kembalian.setText("Input tidak sesuai");
        }
    }
    
