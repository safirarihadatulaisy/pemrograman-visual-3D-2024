    private void totalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_totalActionPerformed
        // TODO add your handling code here:
        if (!nastar.isSelected() && !kastengel.isSelected() && !putrisalju.isSelected() && !cookies.isSelected() && !pastel.isSelected() && !lidahkucing.isSelected()) {
            JOptionPane.showMessageDialog(null, "Silahkan pilih jenis menu roti terlebih dahulu sebelum melanjutkan pemesanan!");
            return;
        }
        
        if (!tanpatopping.isSelected() && !meses.isSelected() && !susucoklat.isSelected() && !gulamerah.isSelected() && !keju.isSelected()) {
            JOptionPane.showMessageDialog(null, "Silahkan pilih jenis topping sebelum melanjutkan pemesanan");
            return;
        }
        
        if (nastar.isSelected()){
            hargaroti = 38000;
        } else if (kastengel.isSelected()) {
            hargaroti = 15000;
        } else if (putrisalju.isSelected()){
            hargaroti = 30000;
        } else if (cookies.isSelected()){
            hargaroti = 18000;
        } else if (pastel.isSelected()){
            hargaroti = 25000;
        } else if (lidahkucing.isSelected()){
            hargaroti = 20000;
        }
        
        if (tanpatopping.isSelected()){
            hargatopping = 0;
        } else if (meses.isSelected()){
            hargatopping = 3000;
        } else if (keju.isSelected()){
            hargatopping = 5000;
        } else if (susucoklat.isSelected()){
            hargatopping = 5000;
        } else if (gulamerah.isSelected()){
            hargatopping = 7500;
        }
        
        double totalharga = (hargaroti + hargatopping)*jumlahpesanan;
        
        if(totalharga > 100000){
            totalharga = totalharga * 0.95;
        }
        
        tampilantotal.setText(String.format("Rp %.2f", totalharga));
        
        if (cash.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Masukkkan jumlah uang untuk pembayaran!");
            return;
        }
        
        try {
            double uangpembeli = Double.parseDouble(cash.getText());
            
            if (uangpembeli < totalharga){
                JOptionPane.showMessageDialog(null, "Uang anda kurang!");
                return;
            }
            
            double kembalian = uangpembeli - totalharga;           
            kembaliann.setText(String.format("Rp %.2f", kembalian));
        } catch (NumberFormatException a){
            JOptionPane.showMessageDialog(null, "Masukkan input uang yang valid!");
        }
    }//GEN-LAST:event_totalActionPerformed

